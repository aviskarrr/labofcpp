#include<iostream> 
using namespace std;
int main(){
    int a;
    float b;
    char c;
    double d;
    cout<<"int size is "<<sizeof(a)<<endl;
    cout<<"float size is "<<sizeof(b)<<endl;
    cout<<"char size is "<<sizeof(c)<<endl;
    cout<<"double size is "<<sizeof(d)<<endl;
    short int aa;
    long int bb;
    cout<<"short int size is "<<sizeof(aa)<<endl;
    cout<<"long int size is "<<sizeof(bb)<<endl;
    return 0;
}